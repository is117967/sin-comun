package com.insite.comun.model;

public class Bitacora { 
	  private int bitacoraId;
	  private int usuarioId;
	  private int fecha;
	  private int accionId;
	  private int aplicacionId;
	public int getBitacoraId() {
		return bitacoraId;
	}
	public void setBitacoraId(int bitacoraId) {
		this.bitacoraId = bitacoraId;
	}
	public int getUsuarioId() {
		return usuarioId;
	}
	public void setUsuarioId(int usuarioId) {
		this.usuarioId = usuarioId;
	}
	public int getFecha() {
		return fecha;
	}
	public void setFecha(int fecha) {
		this.fecha = fecha;
	}
	public int getAccionId() {
		return accionId;
	}
	public void setAccionId(int accionId) {
		this.accionId = accionId;
	}
	public int getAplicacionId() {
		return aplicacionId;
	}
	public void setAplicacionId(int aplicacionId) {
		this.aplicacionId = aplicacionId;
	}
	  
	  

}
